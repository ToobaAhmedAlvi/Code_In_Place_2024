  
def add(a,b):
    return int(a)+int(b)

def subract(a,b):
    return int(a)-int(b)

def multiply(a,b):
    return int(a)*int(b)

def divide(a,b):
    return int(a)/int(b)
   
#necessary to assign a function to a variable for (return functions)   

sum=add(3,6)
minus=subract(3,6)
multiply=multiply(3,6)
divide=divide(3,6)


print("The sum is:",sum)
print("The difference is:",minus)
print("The product is:",multiply)
print("The quotient is:",divide)

