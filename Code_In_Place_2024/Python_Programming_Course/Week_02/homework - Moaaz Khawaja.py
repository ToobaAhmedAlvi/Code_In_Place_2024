def mercury_weight():
    MERCURY_EQUIVALENT_WEIGHT = 37.6
    user_input = float(input("Enter your weight on Earth: "))
    print("Weight on Earth:", user_input)
    result = user_input * MERCURY_EQUIVALENT_WEIGHT
    print("Weight on Mercury:", round(result, 2))

mercury_weight()

